function resolve(path: string, source: Messages): string | undefined {
  return path.split(".").reduce<string | Messages | undefined>((acc, key) => {
    if (!acc || typeof acc === "string") return undefined;
    return acc[key];
  }, source) as string | undefined;
}

export function t(locale: Locale, key: string): string {
  return resolve(key, messages[locale]) ?? resolve(key, messages["pt-BR"]) ?? key;
}

export function getLocaleLabel(locale: Locale): string {
  return locale === "en" ? messages.en.common.localeEn : messages["pt-BR"].common.localePt;
}
